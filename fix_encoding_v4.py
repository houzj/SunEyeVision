def try_decode(raw: bytes) -> Tuple[Optional[str], Optional[str]]:
    """尝试用多种编码解码"""
    encodings = [
        'utf-8-sig', 'utf-8', 
        'gbk', 'gb2312', 'gb18030',
        'utf-16-le', 'utf-16-be', 'utf-16',
        'cp936', 'cp950', 'big5',
        'latin-1', 'iso-8859-1',
    ]
    
    for enc in encodings:
        try:
            text = raw.decode(enc)
            # 检查解码结果是否合理（没有太多替换字符）
            if text.count('\ufffd') < len(text) * 0.1:  # 替换字符不超过10%
                return text, enc
        except:
            continue
    
    # 最后尝试用errors='replace'
    try:
        return raw.decode('utf-8', errors='replace'), 'utf-8 (with replacements)'
    except:
        return None, None

def fix_content(content: str) -> Tuple[str, int]:
    """修复内容"""
    fix_count = 0
    
    if '\ufffd' not in content:
        return content, 0
    
    for wrong, correct in ENCODING_FIXES.items():
        if wrong in content:
            count = content.count(wrong)
            content = content.replace(wrong, correct)
            fix_count += count
    
    for pattern, correct in CONTEXT_FIXES:
        matches = re.findall(pattern, content)
        if matches:
            content = re.sub(pattern, correct, content)
            fix_count += len(matches)
    
    return content, fix_count

def find_cs_files(root_dir: Path) -> List[Path]:
    """查找C#文件"""
    cs_files = []
    for root, dirs, files in os.walk(root_dir):
        dirs[:] = [d for d in dirs if d not in ['obj', 'bin', '.git']]
        for file in files:
            if file.endswith('.cs'):
                cs_files.append(Path(root) / file)
    return cs_files

def main():
    print("=" * 70)
    print("SunEyeVision 编码问题批量修复脚本 v4")
    print("=" * 70)
    
    src_dir = Path(__file__).parent / 'src'
    if not src_dir.exists():
        print("错误: 找不到src目录")
        return
    
    print(f"\n扫描目录: {src_dir}")
    cs_files = find_cs_files(src_dir)
    print(f"找到 {len(cs_files)} 个C#文件")
    
    print("\n分析编码问题...")
    results = []
    
    for file_path in cs_files:
        try:
            with open(file_path, 'rb') as f:
                raw = f.read()
            
            content, encoding = try_decode(raw)
            if content is None:
                print(f"  无法解码: {file_path.name}")
                continue
            
            if '\ufffd' in content:
                fixed, count = fix_content(content)
                if count > 0:
                    rel = file_path.relative_to(src_dir.parent)
                    results.append((file_path, str(rel), encoding, count, fixed))
        except Exception as e:
            print(f"  错误: {file_path.name}: {e}")
    
    print(f"\n发现 {len(results)} 个文件可修复")
    
    if not results:
        print("没有发现可修复的问题!")
        return
    
    # 排序显示
    results.sort(key=lambda x: x[3], reverse=True)
    print("\n问题文件列表 (前30个):")
    print("-" * 70)
    for _, rel, enc, count, _ in results[:30]:
        print(f"  {rel} [{enc}] ({count}处)")
    if len(results) > 30:
        print(f"  ... 还有 {len(results) - 30} 个文件")
    
    total = sum(r[3] for r in results)
    print(f"\n总计: {len(results)} 个文件, {total} 处问题")
    
    print("\n" + "=" * 70)
    print("警告: 修复将覆盖原文件并转换为UTF-8编码!")
    print("=" * 70)
    
    ans = input("\n执行修复? (y/n): ").strip().lower()
    if ans != 'y':
        print("已取消")
        return
    
    print("\n修复中...")
    fixed = 0
    for file_path, rel, enc, count, content in results:
        try:
            with open(file_path, 'w', encoding='utf-8') as f:
                f.write(content)
            print(f"  OK: {rel} ({count}处)")
            fixed += 1
        except Exception as e:
            print(f"  失败: {rel}: {e}")
    
    print(f"\n完成! 修复了 {fixed} 个文件")

if __name__ == '__main__':
    main()
